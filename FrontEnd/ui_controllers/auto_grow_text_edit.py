"""
auto_grow_text_edit.py
======================
Drop-in composite widget for mainUI.py's AutoGrowTextEdit instances.

LAYOUT (everything lives INSIDE the rounded QFrame):
─────────────────────────────────────────────────────────────────
  QFrame#iFrame
    ├── _strip        chip row (hidden by default)
    │     ├── _LoadingChip  (gif + "Uploading…" + filename)
    │     └── _AttachChip   (ext badge/thumb + filename + ✕)
    ├── _edit         _InnerEdit (grows between TEXT_MIN … TEXT_MAX)
    └── _btn_row      bottom toolbar row
          ├── add_btn      QPushButton  (public – wire to open_document)
          ├── [stretch]
          └── search_btn   QPushButton  (public – wire to send/stop)
─────────────────────────────────────────────────────────────────

Public attributes
-----------------
add_btn    : QPushButton  – the + attachment button (bottom-left)
search_btn : QPushButton  – the 🔍/⏹ button        (bottom-right)

Signals
-------
height_changed(int)
    Emitted on every height change.
    • main.py connects on text_prompt_2 → resizes frame_2 (upward growth).

pyuic5 compatibility stubs
--------------------------
mainUI.py (generated by pyuic5) calls several QTextEdit / QFrame methods on
this object.  They are silently ignored or proxied so that even an
un-patched pyuic5 output works without manual editing.
"""

